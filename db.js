exports.getCustomerById = function (id) {
  for (var i = 0; i < customers.length; i++) {
    if (customers[ i ].id == id) {
      return customers[ i ];
    }
  }
}

exports.saveCustomer = function (id, customer) {
  if (id == 0) {
    customer.id = customers.length + 1;
    customers.push(customer);
  } else {
    for (var i = 0; i < customers.length; i++) {
      if (customers[ i ].id == id) {
        customers[ i ].first_name = customer.name;
        customers[ i ].email = customer.email;
        customers[ i ].gender = customer.gender;
      }
    }
  }
}

exports.removeCustomer = function (id) {
  for (var i = 0; i < customers.length; i++) {
    if (customers[ i ].id == id) {
      customers.splice(i, 1);
    }
  }
}